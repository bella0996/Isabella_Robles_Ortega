# Crear un arreglo
numeros = [10, 20, 30, 40, 50]

# Mostrar todo el arreglo
print("Arreglo completo:", numeros)

# Acceder a un elemento
print("Primer elemento:", numeros[0])

# Modificar un elemento
numeros[2] = 35

# Recorrer el arreglo
for num in numeros:
    print("Elemento:", num)
